package serial;

import java.io.Serializable;

/**
 *
 * @author kingdragon
 */
public class Point implements Serializable{
    
    private double x,y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
    
    public String toString()
    {
        return "( x , y) = ("+x+" , "+y+" )";
    }
    
}
