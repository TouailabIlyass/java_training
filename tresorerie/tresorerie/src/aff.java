    public void Afficher(){
        PreparedStatement state;
               
        ResultSet rs;
        
    try{
          state=con.prepareStatement("select * from attestation_rib");
           rs=state.executeQuery();
dts.setRowCount(0);
      while(rs.next()){
            
            int id=rs.getInt(1);
            int codef=rs.getInt(2);
            String dates=rs.getString(3);
            String cmd=rs.getString(4);
            int banque=rs.getInt(5);
            String datee=rs.getString(6);
            String datem=rs.getString(7);
           
            Object[] stock={id,codef,dates,cmd,banque,datee,datem};
            dts.addRow(stock);
            }
      jTable1.setModel(dts);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "erreur dans l'affichage du stock"+ex.getMessage());
        }
    }
